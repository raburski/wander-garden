export function convertCurrencySymbol(symbol) {
  if (symbol && typeof symbol === 'string' && symbol.length === 3) return symbol.toUpperCase()
  const code = currencyMap[symbol];
  if (code) {
    return code;
  } else {
    throw new Error(`Currency symbol "${symbol}" not found.`);
  }
}
  
export function isPotentialPostcode(string) {
  const genericPattern = /^[0-9A-Z\s-]+$/
  return genericPattern.test(string)
}

export function getAddressComponents(string) {
  const parts = string.split(',').map(s => s.trim()).filter(s => !isPotentialPostcode(s))
  const country = parts.pop()
  const city = parts.pop().split(' ').filter(s => !isPotentialPostcode(s)).join(' ')
  const address = parts.join(', ')

  return {
      address,
      city,
      country,
      cc: getCountryCode(country)
  }
}